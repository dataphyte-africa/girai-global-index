/**
 * Minimal country shape used by the home-page hero globe. Decoupled
 * from the full {@link CountryRanking} shape so the globe doesn't have
 * to know about score data.
 */
export type GlobeCountry = {
  iso3: string;
  name: string;
  region: string;
  rankGlobal: number | null;
  girai: number | null;
};

export type Country = GlobeCountry & {
  lat: number;
  lng: number;
  flag: string;
};
 
 export type ArcPosition = {
   order: number;
   startLat: number;
   startLng: number;
   endLat: number;
   endLng: number;
   arcAlt: number;
   color: string;
 };
 
 const ARC_COLORS = ["#4fd1c5", "#38b2ac", "#2dd4bf"];
 
/**
 * Select N countries with a mix from all GIRAI regions.
 */
export function selectMixedCountries(
  rankings: GlobeCountry[],
  count: number = 20
): Country[] {
  const byRegion = new Map<string, GlobeCountry[]>();
  for (const r of rankings) {
    const region = r.region || "Other";
    if (!byRegion.has(region)) byRegion.set(region, []);
    byRegion.get(region)!.push(r);
  }

  const selected: Country[] = [];
  const regions = Array.from(byRegion.keys());

  while (selected.length < count) {
    let added = 0;
    for (const region of regions) {
      const pool = byRegion.get(region)!;
      if (pool.length === 0 || selected.length >= count) continue;
      const idx = Math.floor(Math.random() * pool.length);
      const r = pool.splice(idx, 1)[0];
      const coords = countryCoordinates[r.iso3];
      const flag = countryFlags[r.iso3];
      if (coords && flag) {
        selected.push({
          ...r,
          lat: coords.lat,
          lng: coords.lng,
          flag,
        });
        added++;
      }
    }
    if (added === 0) break;
  }

  return selected.slice(0, count);
}
 
 /**
  * Generate random arc data between countries for visual effect.
  */
 export function generateArcData(
   countries: Country[],
   arcCount: number = 15
 ): ArcPosition[] {
   const arcs: ArcPosition[] = [];
   const maxArcs = Math.min(arcCount, Math.floor((countries.length * (countries.length - 1)) / 2));
 
   const used = new Set<string>();
   const key = (i: number, j: number) => [i, j].sort((a, b) => a - b).join(",");
 
   while (arcs.length < maxArcs) {
     const i = Math.floor(Math.random() * countries.length);
     const j = Math.floor(Math.random() * countries.length);
     if (i === j) continue;
     const k = key(i, j);
     if (used.has(k)) continue;
     used.add(k);
 
     const start = countries[i];
     const end = countries[j];
     arcs.push({
       order: arcs.length,
       startLat: start.lat,
       startLng: start.lng,
       endLat: end.lat,
       endLng: end.lng,
       arcAlt: 0.3 + Math.random() * 0.4,
       color: ARC_COLORS[arcs.length % ARC_COLORS.length],
     });
   }
 
   return arcs;
 }